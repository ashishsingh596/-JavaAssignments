package com.assignment.java;

public class PrimitiveDataType {

	    static boolean t;
		static int i;
		static char c;
		static long l;
		static double d;
		static float f;
		static byte b;
		static short s;
	

	static void Display() {
		System.out.println("boolean initial=" + t );
		System.out.println("int initial=" + i );
		System.out.println("char initial=" + c );
		System.out.println("long initial=" + l );
		System.out.println("double initial=" + d );
		System.out.println("float initial=" + f );
		System.out.println("byte initial=" + b );
		System.out.println("short initial=" + s);
	}
	public static void main(String[] args) {
		Display();
		
	}
}
